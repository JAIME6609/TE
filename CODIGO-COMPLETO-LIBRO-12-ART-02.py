def generate_demo_dataset(n: int = 4000, seed: int = 42) -> pd.DataFrame:
    """
    Creates a mixed-type, governance-styled demo dataset (tabular + time index + group + target).
    This is only used when --data_path is omitted.
    """
    rng = np.random.default_rng(seed)
    # numeric
    income = rng.lognormal(mean=10.2, sigma=0.6, size=n)
    age = rng.integers(18, 85, size=n)
    visits = rng.poisson(lam=3.0, size=n)
    # categorical
    region = rng.choice(["North", "Center", "South"], size=n, p=[0.28, 0.44, 0.28])
    program = rng.choice(["A", "B", "C", "D"], size=n, p=[0.25, 0.30, 0.25, 0.20])
    # group (protected-like)
    group = rng.choice(["G1", "G2", "G3"], size=n, p=[0.55, 0.30, 0.15])
    # time
    start = np.datetime64("2023-01-01")
    days = rng.integers(0, 730, size=n)
    date = start + days.astype("timedelta64[D]")
    # target: binary risk-like outcome
    logit = -3.0 + 0.000015 * income + 0.02 * (age - 40) + 0.12 * visits + (program == "D") * 0.6 + (group == "G3") * 0.25
    p = 1 / (1 + np.exp(-logit))
    y = rng.binomial(1, np.clip(p, 0.01, 0.99), size=n)

    df = pd.DataFrame(
        {
            "income": income,
            "age": age,
            "visits": visits,
            "region": region,
            "program": program,
            "group": group,
            "date": pd.to_datetime(date.astype("datetime64[ns]")),
            "target": y.astype(int),
            "sensitive_attr": rng.choice(["S0", "S1"], size=n, p=[0.7, 0.3]),
        }
    )
    # inject missingness
    miss_idx = rng.choice(n, size=int(0.03 * n), replace=False)
    df.loc[miss_idx, "income"] = np.nan
    miss_idx = rng.choice(n, size=int(0.02 * n), replace=False)
    df.loc[miss_idx, "program"] = np.nan
    return df


def load_dataset(path: Optional[str]) -> pd.DataFrame:
    if path is None:
        return generate_demo_dataset()
    path = str(path)
    if not os.path.exists(path):
        raise FileNotFoundError(f"Data path not found: {path}")
    ext = os.path.splitext(path)[1].lower()
    if ext in [".csv"]:
        return pd.read_csv(path)
    if ext in [".xlsx", ".xls"]:
        return pd.read_excel(path)
    if ext in [".parquet"]:
        return pd.read_parquet(path)
    raise ValueError(f"Unsupported file extension: {ext}")


def run_pipeline(cfg: RunConfig, data_path: Optional[str], output_dir: Optional[str]) -> str:
    # output layout
    root = output_dir or cfg.output_root
    run_root = os.path.join(root, cfg.run_id)
    if os.path.exists(run_root):
        shutil.rmtree(run_root)
    ensure_dir(run_root)

    out_51 = os.path.join(run_root, "5_1_experimental_design")
    out_52 = os.path.join(run_root, "5_2_utility_privacy_tradeoffs")
    out_53 = os.path.join(run_root, "5_3_topological_auditing")
    ensure_dir(out_51); ensure_dir(out_52); ensure_dir(out_53)

    # load data
    df = load_dataset(data_path)

    # configure evaluation columns
    if cfg.evaluation.time_col is None and "date" in df.columns:
        cfg.evaluation.time_col = "date"
    if cfg.evaluation.target_col is None and "target" in df.columns:
        cfg.evaluation.target_col = "target"
    if cfg.evaluation.group_col is None and "group" in df.columns:
        cfg.evaluation.group_col = "group"
    if cfg.evaluation.sensitive_col is None and "sensitive_attr" in df.columns:
        cfg.evaluation.sensitive_col = "sensitive_attr"

    # ensure time parsing
    if cfg.evaluation.time_col is not None and cfg.evaluation.time_col in df.columns:
        df[cfg.evaluation.time_col] = pd.to_datetime(df[cfg.evaluation.time_col], errors="coerce")

    # infer types
    coltypes = infer_column_types(df, time_col=cfg.evaluation.time_col, target_col=cfg.evaluation.target_col)

    # 5.1 artifacts
    section_5_1_experimental_design(df, coltypes, cfg, out_51)

    # train/test split (temporal or random)
    target_col = cfg.evaluation.target_col
    time_col = cfg.evaluation.time_col
    df_model = df.copy()

    # drop rows without target for downstream tasks
    if target_col is not None and target_col in df_model.columns:
        df_model = df_model.dropna(subset=[target_col])

    if time_col is not None and cfg.evaluation.temporal_split and time_col in df_model.columns:
        # chronological split to prevent leakage
        df_model = df_model.dropna(subset=[time_col]).sort_values(time_col)
        n = len(df_model)
        cut = int((1.0 - cfg.evaluation.test_size) * n)
        df_train = df_model.iloc[:cut].copy()
        df_test = df_model.iloc[cut:].copy()
    else:
        # random split; stratify if classification/binary
        strat = None
        if target_col is not None and target_col in df_model.columns:
            ytemp = df_model[target_col]
            if infer_task_type(ytemp) == "classification":
                strat = ytemp
        df_train, df_test = train_test_split(
            df_model, test_size=cfg.evaluation.test_size, random_state=cfg.evaluation.random_state, stratify=strat
        )

    # define feature columns for preprocessing
    # exclude time and target for the model matrix; keep group/sensitive as features if present (auditing may require)
    drop_cols = []
    if time_col is not None and time_col in df_train.columns:
        drop_cols.append(time_col)
    if target_col is not None and target_col in df_train.columns:
        drop_cols.append(target_col)

    df_train_X = df_train.drop(columns=drop_cols, errors="ignore")
    df_test_X = df_test.drop(columns=drop_cols, errors="ignore")

    # Preprocessor
    pre = MixedTabularPreprocessor(coltypes["numeric"], coltypes["categorical"], coltypes["boolean"])
    pre.fit(df_train_X)
    X_train = pre.transform(df_train_X)
    X_test = pre.transform(df_test_X)

    # labels for downstream
    y_train = None
    y_test = None
    if target_col is not None and target_col in df_train.columns:
        y_train = df_train[target_col].to_numpy()
        y_test = df_test[target_col].to_numpy()
        # enforce numeric labels for classification if possible
        if infer_task_type(pd.Series(y_train)) == "classification":
            # map to 0..K-1 if needed
            uniq = sorted(pd.unique(pd.Series(y_train)))
            mapping = {v: i for i, v in enumerate(uniq)}
            y_train = np.array([mapping[v] for v in y_train])
            y_test = np.array([mapping[v] for v in y_test])

    # generator portfolio training
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    syn_by_gen: Dict[str, List[pd.DataFrame]] = {}

    # Ensure target/time are carried into synthesis for evaluation convenience:
    # here, generators are trained on full df_train (including target/time) but sampled outputs drop time by default
    # for topology/time windows, time is retained if present.
    df_train_for_gen = df_train.copy()

    # For preprocessing inside generators, build a preprocessor on X-only columns and inverse back to X-only.
    # Then re-attach target/time by bootstrapping from real train.
    for gen_name in cfg.generators.generators:
        syn_by_gen[gen_name] = []
        for r in range(cfg.generators.replicas):
            seed = cfg.generators.base_seed + 1000 * cfg.generators.generators.index(gen_name) + r
            set_global_seed(seed)

            if gen_name == "bootstrap":
                gen = BootstrapNoiseGenerator(numeric_cols=coltypes["numeric"], noise_scale=cfg.generators.bootstrap_noise_scale)
                gen.fit(df_train_for_gen, pre, cfg)
                syn = gen.sample(len(df_test), seed=seed)
            elif gen_name == "copula":
                gen = GaussianCopulaGenerator(coltypes["numeric"], coltypes["categorical"], coltypes["boolean"])
                gen.fit(df_train_for_gen, pre, cfg)
                syn = gen.sample(len(df_test), seed=seed)
            elif gen_name == "vae":
                # Train VAE on X-only
                vae_gen = VAEGenerator(pre=pre, cfg=cfg.generators, device=device)
                vae_gen.fit(df_train_X, pre, cfg)
                syn_X = vae_gen.sample(len(df_test), seed=seed)
                # attach target/time/group/sensitive by sampling from train (keeps marginal distributions)
                syn = syn_X.copy()
                for extra in [target_col, time_col]:
                    if extra is not None and extra in df_train.columns:
                        rng = np.random.default_rng(seed + 123)
                        syn[extra] = rng.choice(df_train[extra].to_numpy(), size=len(syn), replace=True)
                # also attach group and sensitive if they were dropped
                for extra in [cfg.evaluation.group_col, cfg.evaluation.sensitive_col]:
                    if extra is not None and extra in df_train.columns and extra not in syn.columns:
                        rng = np.random.default_rng(seed + 321)
                        syn[extra] = rng.choice(df_train[extra].astype(str).to_numpy(), size=len(syn), replace=True)
            else:
                raise ValueError(f"Unknown generator: {gen_name}")

            # Ensure required columns exist (target/time/group/sensitive) for downstream audits.
            for extra in [target_col, time_col, cfg.evaluation.group_col, cfg.evaluation.sensitive_col]:
                if extra is not None and extra in df_train.columns and extra not in syn.columns:
                    rng = np.random.default_rng(seed + 777)
                    syn[extra] = rng.choice(df_train[extra].to_numpy(), size=len(syn), replace=True)

            syn_by_gen[gen_name].append(syn)

            # save the synthetic replica dataset itself (auditable artifact)
            save_df(syn, os.path.join(out_52, f"synthetic_{gen_name}_rep{r}.csv"))

    # 5.2 artifacts (utility/privacy trade-offs)
    analysis_5_2 = section_5_2_utility_privacy(
        df_train=df_train,
        df_test=df_test,
        pre=pre,
        X_train=X_train,
        X_test=X_test,
        y_train=y_train,
        y_test=y_test,
        coltypes=coltypes,
        cfg=cfg,
        syn_by_gen=syn_by_gen,
        out_dir=out_52,
    )

    # Add Pareto frontier table based on composite scores
    comp = analysis_5_2["composite_df"].copy()
    frontier = pareto_frontier(comp, x="privacy_risk_score", y="utility_score", minimize_x=True, maximize_y=True)
    save_df(frontier, os.path.join(out_52, "pareto_frontier_utility_privacy.csv"))

    # 5.3 artifacts (topological auditing outcomes)
    section_5_3_topological(
        df_train=df_train,
        df_test=df_test,
        pre=pre,
        X_train=X_train,
        X_test=X_test,
        coltypes=coltypes,
        cfg=cfg,
        syn_by_gen=syn_by_gen,
        analysis_5_2=analysis_5_2,
        out_dir=out_53,
    )

    # Run metadata card
    card = {
        "run_id": cfg.run_id,
        "created_at": _dt.datetime.now().isoformat(),
        "data_path": data_path or "DEMO_DATASET_GENERATED_IN_SCRIPT",
        "output_root": run_root,
        "governance_scope": dataclasses.asdict(cfg.governance),
        "threat_model": dataclasses.asdict(cfg.threat),
        "evaluation": dataclasses.asdict(cfg.evaluation),
        "generator_config": dataclasses.asdict(cfg.generators),
        "topology_config": dataclasses.asdict(cfg.topology),
        "acceptance_config": dataclasses.asdict(cfg.acceptance),
    }
    save_json(card, os.path.join(run_root, "run_card.json"))

    # Minimal README describing where to find Section 5 artifacts
    readme = (
        "RESULTS FOLDER STRUCTURE (mapped to Section 5)\n"
        "---------------------------------------------\n"
        "5_1_experimental_design/\n"
        "  - dataset_profile.csv\n"
        "  - governance_scope.csv\n"
        "  - adversary_assumptions.csv\n"
        "  - inferred_column_types.csv\n"
        "  - generator_portfolio_and_replicas.csv\n"
        "  - missingness_top30.png\n"
        "\n"
        "5_2_utility_privacy_tradeoffs/\n"
        "  - synthetic_<generator>_rep<k>.csv\n"
        "  - fidelity_metrics_by_generator_replica.csv\n"
        "  - downstream_task_transfer.csv (if target provided)\n"
        "  - fairness_diagnostics_binary.csv + fairness_tpr_by_group.png (if binary + group)\n"
        "  - privacy_risk_metrics.csv\n"
        "  - membership_attack_roc.png\n"
        "  - utility_privacy_composite.csv\n"
        "  - utility_privacy_tradeoff_scatter.png\n"
        "  - pareto_frontier_utility_privacy.csv\n"
        "\n"
        "5_3_topological_auditing/\n"
        "  - selected_best_synthetic.txt\n"
        "  - persistence_summaries_global.csv\n"
        "  - diagram_distances_global.csv\n"
        "  - betti_curves_global.png\n"
        "  - subgroup_topology_distances.csv + subgroup_bottleneck_H0.png (if group)\n"
        "  - temporal_window_topology.csv + temporal_window_bottleneck_H0.png (if time)\n"
        "\n"
        "Reproducibility: See run_card.json for seeds and configuration.\n"
    )
    save_text(readme, os.path.join(run_root, "README.txt"))

    return run_root


def parse_args() -> argparse.Namespace:
    p = argparse.ArgumentParser(description="Synthetic Data Auditing Pipeline (Utility, Privacy, Topology).")
    p.add_argument("--data_path", type=str, default=None, help="Path to CSV/XLSX/Parquet dataset. If omitted, uses a demo dataset.")
    p.add_argument("--output_dir", type=str, default="results", help="Output root directory.")
    p.add_argument("--target", type=str, default=None, help="Target column for downstream task transfer.")
    p.add_argument("--group_col", type=str, default=None, help="Group column for subgroup/fairness audits.")
    p.add_argument("--time_col", type=str, default=None, help="Time column for temporal splits/windows (optional).")
    p.add_argument("--temporal_split", action="store_true", help="Use chronological split if time_col is provided.")
    p.add_argument("--sensitive_col", type=str, default=None, help="Sensitive attribute for attribute inference risk (optional).")
    p.add_argument("--quasi_ids", type=str, default=None, help="Comma-separated quasi-identifier columns for linkage risk (optional).")
    p.add_argument("--replicas", type=int, default=3, help="Number of synthetic replicas per generator.")
    p.add_argument("--seed", type=int, default=2026, help="Base random seed.")
    p.add_argument("--vae_epochs", type=int, default=None, help="Epochs for the Tabular VAE generator (optional).")
    p.add_argument("--ae_epochs", type=int, default=None, help="Epochs for the embedding autoencoder (optional).")
    p.add_argument("--subsample_n", type=int, default=None, help="Point-cloud subsample size for persistent homology (optional).")
    p.add_argument("--rips_max_dim", type=int, default=None, choices=[0, 1], help="Max homology dimension for Rips (0=H0, 1=H0+H1).")
    return p.parse_args()


def main() -> int:
    args = parse_args()
    cfg = RunConfig()
    cfg.output_root = args.output_dir
    cfg.generators.replicas = int(args.replicas)
    cfg.generators.base_seed = int(args.seed)
    cfg.evaluation.target_col = args.target or cfg.evaluation.target_col
    cfg.evaluation.group_col = args.group_col or cfg.evaluation.group_col
    cfg.evaluation.time_col = args.time_col or cfg.evaluation.time_col
    cfg.evaluation.temporal_split = bool(args.temporal_split)
    cfg.evaluation.sensitive_col = args.sensitive_col or cfg.evaluation.sensitive_col
    if args.quasi_ids:
        cfg.evaluation.quasi_id_cols = [c.strip() for c in args.quasi_ids.split(",") if c.strip()]

    if args.vae_epochs is not None:
        cfg.generators.vae_epochs = int(args.vae_epochs)
    if args.ae_epochs is not None:
        cfg.topology.ae_epochs = int(args.ae_epochs)
    if args.subsample_n is not None:
        cfg.topology.subsample_n = int(args.subsample_n)
    if args.rips_max_dim is not None:
        cfg.topology.rips_max_dim = int(args.rips_max_dim)

    run_root = run_pipeline(cfg, data_path=args.data_path, output_dir=args.output_dir)
    print(f"Run completed. Results written to: {run_root}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
