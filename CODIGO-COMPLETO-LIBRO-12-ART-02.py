class AutoEncoder(nn.Module):
    def __init__(self, input_dim: int, embed_dim: int, hidden_dim: int):
        super().__init__()
        self.enc1 = nn.Linear(input_dim, hidden_dim)
        self.enc2 = nn.Linear(hidden_dim, hidden_dim)
        self.emb = nn.Linear(hidden_dim, embed_dim)
        self.dec1 = nn.Linear(embed_dim, hidden_dim)
        self.dec2 = nn.Linear(hidden_dim, hidden_dim)
        self.out = nn.Linear(hidden_dim, input_dim)

    def encode(self, x: torch.Tensor) -> torch.Tensor:
        h = F.relu(self.enc1(x))
        h = F.relu(self.enc2(h))
        return self.emb(h)

    def decode(self, z: torch.Tensor) -> torch.Tensor:
        h = F.relu(self.dec1(z))
        h = F.relu(self.dec2(h))
        return self.out(h)

    def forward(self, x: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor]:
        z = self.encode(x)
        recon = self.decode(z)
        return recon, z



def train_autoencoder(
    X_train: np.ndarray,
    topo_cfg: TopologyConfig,
    seed: int,
    device: torch.device,
) -> AutoEncoder:
    """
    Trains a deterministic autoencoder on the real training representation space.
    The encoder is then reused to embed real test points and synthetic points,
    ensuring a *shared* representation geometry for topology auditing.
    """
    set_global_seed(seed)

    X = X_train.astype(np.float32)
    # Subsample to keep embedding training feasible while preserving geometry signals
    if X.shape[0] > topo_cfg.ae_max_train_rows:
        rng = np.random.default_rng(54321)
        idx = rng.choice(X.shape[0], size=topo_cfg.ae_max_train_rows, replace=False)
        X = X[idx]

    Xtr = torch.from_numpy(X).to(device)
    ds = TensorDataset(Xtr)
    dl = DataLoader(ds, batch_size=topo_cfg.ae_batch_size, shuffle=True, drop_last=False)

    model = AutoEncoder(input_dim=X_train.shape[1], embed_dim=topo_cfg.embedding_dim, hidden_dim=topo_cfg.ae_hidden_dim).to(device)
    opt = torch.optim.Adam(model.parameters(), lr=topo_cfg.ae_lr)

    model.train()
    for _ in range(topo_cfg.ae_epochs):
        for (xb,) in dl:
            opt.zero_grad()
            recon, _z = model(xb)
            loss = F.mse_loss(recon, xb, reduction="mean")
            loss.backward()
            opt.step()

    return model


def encode_with_autoencoder(model: AutoEncoder, X: np.ndarray, device: torch.device) -> np.ndarray:
    model.eval()
    Xt = torch.from_numpy(X.astype(np.float32)).to(device)
    with torch.no_grad():
        Z = model.encode(Xt).detach().cpu().numpy()
    return Z


