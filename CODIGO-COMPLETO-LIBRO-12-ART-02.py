def infer_column_types(df: pd.DataFrame, time_col: Optional[str], target_col: Optional[str]) -> Dict[str, List[str]]:
    numeric_cols: List[str] = []
    categorical_cols: List[str] = []
    boolean_cols: List[str] = []
    datetime_cols: List[str] = []

    for c in df.columns:
        if c == target_col:
            continue
        if time_col is not None and c == time_col:
            datetime_cols.append(c)
            continue

        s = df[c]
        if pd.api.types.is_bool_dtype(s):
            boolean_cols.append(c)
        elif pd.api.types.is_numeric_dtype(s):
            numeric_cols.append(c)
        elif pd.api.types.is_datetime64_any_dtype(s):
            datetime_cols.append(c)
        else:
            # Heuristic: small-cardinality numeric-looking object is still categorical
            categorical_cols.append(c)

    return {
        "numeric": numeric_cols,
        "categorical": categorical_cols,
        "boolean": boolean_cols,
        "datetime": datetime_cols,
    }


class MixedTabularPreprocessor:
    """
    Produces:
      - X_model: numpy array for sklearn models (numeric scaled + one-hot)
      - X_tensor: torch tensor for neural generators/embeddings
      - Inverse transform back to DataFrame for synthetic samples
    """

    def __init__(self, numeric_cols: List[str], categorical_cols: List[str], boolean_cols: List[str]):
        self.numeric_cols = list(numeric_cols)
        self.categorical_cols = list(categorical_cols)
        self.boolean_cols = list(boolean_cols)

        self.numeric_pipe = Pipeline(
            steps=[
                ("imputer", SimpleImputer(strategy="median")),
                ("scaler", StandardScaler()),
            ]
        )
        self.cat_pipe = Pipeline(
            steps=[
                ("imputer", SimpleImputer(strategy="most_frequent")),
                ("onehot", OneHotEncoder(handle_unknown="ignore", sparse_output=False)),
            ]
        )
        # treat booleans as categorical for stability
        self.bool_pipe = Pipeline(
            steps=[
                ("imputer", SimpleImputer(strategy="most_frequent")),
                ("onehot", OneHotEncoder(handle_unknown="ignore", sparse_output=False)),
            ]
        )

        transformers = []
        if self.numeric_cols:
            transformers.append(("num", self.numeric_pipe, self.numeric_cols))
        if self.categorical_cols:
            transformers.append(("cat", self.cat_pipe, self.categorical_cols))
        if self.boolean_cols:
            transformers.append(("bool", self.bool_pipe, self.boolean_cols))

        self.ct = ColumnTransformer(transformers=transformers, remainder="drop")
        self._fitted = False

        # for inverse mapping
        self._cat_categories: Dict[str, List[Any]] = {}
        self._bool_categories: Dict[str, List[Any]] = {}
        self._onehot_slices: Dict[str, Tuple[int, int]] = {}
        self._feature_names: List[str] = []
        self._numeric_scaler: Optional[StandardScaler] = None

    def fit(self, df: pd.DataFrame) -> "MixedTabularPreprocessor":
        self.ct.fit(df)
        self._fitted = True

        self._feature_names = list(self.ct.get_feature_names_out())

        # Extract scalers and categories
        if self.numeric_cols:
            num_pipe: Pipeline = self.ct.named_transformers_["num"]
            self._numeric_scaler = num_pipe.named_steps["scaler"]

        start = 0
        # numeric slice first if exists
        if self.numeric_cols:
            end = start + len(self.numeric_cols)
            start = end

        if self.categorical_cols:
            cat_pipe: Pipeline = self.ct.named_transformers_["cat"]
            ohe: OneHotEncoder = cat_pipe.named_steps["onehot"]
            cats = ohe.categories_
            # compute slices for each categorical col
            idx = start
            for col, cat_list in zip(self.categorical_cols, cats):
                k = len(cat_list)
                self._cat_categories[col] = list(cat_list)
                self._onehot_slices[col] = (idx, idx + k)
                idx += k
            start = idx

        if self.boolean_cols:
            bool_pipe: Pipeline = self.ct.named_transformers_["bool"]
            ohe: OneHotEncoder = bool_pipe.named_steps["onehot"]
            cats = ohe.categories_
            idx = start
            for col, cat_list in zip(self.boolean_cols, cats):
                k = len(cat_list)
                self._bool_categories[col] = list(cat_list)
                self._onehot_slices[col] = (idx, idx + k)
                idx += k

        return self

    def transform(self, df: pd.DataFrame) -> np.ndarray:
        if not self._fitted:
            raise RuntimeError("Preprocessor must be fitted before transform.")
        X = self.ct.transform(df)
        return np.asarray(X, dtype=np.float32)

    def transform_tensor(self, df: pd.DataFrame, device: torch.device) -> torch.Tensor:
        X = self.transform(df)
        return torch.from_numpy(X).to(device)

    def inverse_samples(self, X: np.ndarray, reference_df: pd.DataFrame) -> pd.DataFrame:
        """
        Approximate inverse transform:
          - numeric: inverse StandardScaler then clip to observed min/max in reference_df
          - categorical/bool: argmax over one-hot block
        """
        if not self._fitted:
            raise RuntimeError("Preprocessor must be fitted before inverse_samples.")

        X = np.asarray(X, dtype=np.float32)
        out: Dict[str, Any] = {}

        # Numeric
        offset = 0
        if self.numeric_cols:
            num_scaled = X[:, : len(self.numeric_cols)]
            scaler = self._numeric_scaler
            assert scaler is not None
            num = scaler.inverse_transform(num_scaled)
            for i, col in enumerate(self.numeric_cols):
                col_min = pd.to_numeric(reference_df[col], errors="coerce").min()
                col_max = pd.to_numeric(reference_df[col], errors="coerce").max()
                v = np.clip(num[:, i], col_min, col_max)
                # try to preserve integers if original looks integer-like
                if pd.api.types.is_integer_dtype(reference_df[col]) or (reference_df[col].dropna().apply(float.is_integer).mean() > 0.95):
                    v = np.round(v).astype(int)
                out[col] = v
            offset = len(self.numeric_cols)

        # Categorical + Boolean
        for col in (self.categorical_cols + self.boolean_cols):
            a, b = self._onehot_slices[col]
            block = X[:, a:b]
            idx = np.argmax(block, axis=1)
            if col in self._cat_categories:
                cats = self._cat_categories[col]
            else:
                cats = self._bool_categories[col]
            out[col] = [cats[int(i)] for i in idx]

        return pd.DataFrame(out)


