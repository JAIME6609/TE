def membership_inference_attack(
    X_members: np.ndarray,
    X_nonmembers: np.ndarray,
    X_syn: np.ndarray,
    seed: int,
) -> Dict[str, Any]:
    """
    Distance-based membership inference proxy:
      score(x) = -min_{s in S_syn} ||x - s||_2
    Attack AUC and advantage = max(TPR - FPR).
    """
    nn = NearestNeighbors(n_neighbors=1, algorithm="auto").fit(X_syn)
    d_mem, _ = nn.kneighbors(X_members, n_neighbors=1, return_distance=True)
    d_non, _ = nn.kneighbors(X_nonmembers, n_neighbors=1, return_distance=True)
    scores = np.concatenate([-d_mem.ravel(), -d_non.ravel()], axis=0)
    labels = np.concatenate([np.ones(len(d_mem)), np.zeros(len(d_non))], axis=0)

    auc = roc_auc_score(labels, scores)
    fpr, tpr, thr = roc_curve(labels, scores)
    adv = float(np.max(tpr - fpr))
    return {
        "membership_auc": float(auc),
        "membership_advantage": float(adv),
        "roc_fpr": fpr.tolist(),
        "roc_tpr": tpr.tolist(),
        "roc_thr": thr.tolist(),
    }


def attribute_inference_risk(
    df_syn: pd.DataFrame,
    df_real_test: pd.DataFrame,
    sensitive_col: str,
    feature_cols: List[str],
    seed: int,
) -> Dict[str, float]:
    """
    Attacker trains a predictor of the sensitive attribute using synthetic data.
    Risk is the improvement over a naive baseline (majority class or mean).
    """
    y_syn = df_syn[sensitive_col]
    X_syn = df_syn[feature_cols]
    y_test = df_real_test[sensitive_col]
    X_test = df_real_test[feature_cols]

    # handle missing
    X_syn = X_syn.copy()
    X_test = X_test.copy()
    for c in feature_cols:
        if pd.api.types.is_numeric_dtype(X_syn[c]):
            med = pd.to_numeric(X_syn[c], errors="coerce").median()
            X_syn[c] = pd.to_numeric(X_syn[c], errors="coerce").fillna(med)
            X_test[c] = pd.to_numeric(X_test[c], errors="coerce").fillna(med)
        else:
            mode = X_syn[c].astype(str).mode().iloc[0] if len(X_syn[c].dropna()) else "MISSING"
            X_syn[c] = X_syn[c].astype(str).fillna(mode)
            X_test[c] = X_test[c].astype(str).fillna(mode)

    # one-hot for attacker model
    cols_num = [c for c in feature_cols if pd.api.types.is_numeric_dtype(X_syn[c])]
    cols_cat = [c for c in feature_cols if c not in cols_num]
    ct = ColumnTransformer(
        transformers=[
            ("num", Pipeline([("imp", SimpleImputer(strategy="median")), ("sc", StandardScaler())]), cols_num),
            ("cat", Pipeline([("imp", SimpleImputer(strategy="most_frequent")), ("oh", OneHotEncoder(handle_unknown="ignore", sparse_output=False))]), cols_cat),
        ],
        remainder="drop",
    )
    Xs = ct.fit_transform(X_syn)
    Xt = ct.transform(X_test)

    # baseline
    if pd.api.types.is_numeric_dtype(y_syn):
        baseline_pred = np.full_like(pd.to_numeric(y_test, errors="coerce").to_numpy(), fill_value=float(pd.to_numeric(y_syn, errors="coerce").mean()))
        y_true = pd.to_numeric(y_test, errors="coerce").to_numpy()
        rmse_base = float(np.sqrt(np.mean((baseline_pred - y_true) ** 2)))
    else:
        y_true = y_test.astype(str).to_numpy()
        maj = y_syn.astype(str).value_counts().idxmax()
        baseline_pred = np.array([maj] * len(y_true), dtype=object)
        acc_base = float(accuracy_score(y_true, baseline_pred))

    # attacker model
    if pd.api.types.is_numeric_dtype(y_syn):
        ytr = pd.to_numeric(y_syn, errors="coerce").fillna(pd.to_numeric(y_syn, errors="coerce").median()).to_numpy()
        reg = RandomForestRegressor(n_estimators=300, random_state=seed, n_jobs=-1)
        reg.fit(Xs, ytr)
        pred = reg.predict(Xt)
        rmse = float(np.sqrt(np.mean((pred - y_true) ** 2)))
        # higher risk if attacker reduces error relative to baseline
        risk = max(0.0, (rmse_base - rmse) / (rmse_base + 1e-12))
        return {
            "attribute_task": "regression_rmse",
            "baseline_rmse": rmse_base,
            "attacker_rmse": rmse,
            "attribute_risk": float(risk),
        }
    else:
        ytr = y_syn.astype(str).to_numpy()
        clf = RandomForestClassifier(n_estimators=400, random_state=seed, n_jobs=-1)
        clf.fit(Xs, ytr)
        pred = clf.predict(Xt)
        acc = float(accuracy_score(y_true, pred))
        risk = max(0.0, acc - acc_base)
        return {
            "attribute_task": "classification_accuracy",
            "baseline_accuracy": acc_base,
            "attacker_accuracy": acc,
            "attribute_risk": float(risk),
        }


def linkage_risk_simulation(
    df_real: pd.DataFrame,
    df_syn: pd.DataFrame,
    quasi_id_cols: List[str],
    seed: int,
) -> Dict[str, float]:
    """
    A governance-oriented linkage proxy:
      - Build a quasi-identifier space and compute the nearest synthetic neighbor for each real record.
      - Linkable if within a small radius (adaptive: quantile of NN distances).
      - Unique if exactly one synthetic record lies within that radius.
    """
    rng = np.random.default_rng(seed)

    # prepare quasi-id feature space
    cols = list(quasi_id_cols)
    Xr = df_real[cols].copy()
    Xs = df_syn[cols].copy()

    # simple mixed encoding
    num_cols = [c for c in cols if pd.api.types.is_numeric_dtype(Xr[c])]
    cat_cols = [c for c in cols if c not in num_cols]
    ct = ColumnTransformer(
        transformers=[
            ("num", Pipeline([("imp", SimpleImputer(strategy="median")), ("sc", StandardScaler())]), num_cols),
            ("cat", Pipeline([("imp", SimpleImputer(strategy="most_frequent")), ("oh", OneHotEncoder(handle_unknown="ignore", sparse_output=False))]), cat_cols),
        ],
        remainder="drop",
    )
    Xr_t = ct.fit_transform(Xr)
    Xs_t = ct.transform(Xs)

    nn1 = NearestNeighbors(n_neighbors=1).fit(Xs_t)
    d, _ = nn1.kneighbors(Xr_t, n_neighbors=1)
    d = d.ravel()
    radius = float(np.quantile(d, 0.05))
    # linkable if unusually close
    linkable = d <= radius
    linkable_rate = float(np.mean(linkable))

    # uniqueness within radius
    nnk = NearestNeighbors(radius=radius).fit(Xs_t)
    neigh = nnk.radius_neighbors(Xr_t, return_distance=False)
    uniq = np.array([len(ix) == 1 for ix in neigh], dtype=bool)
    uniqueness_rate = float(np.mean(uniq))

    return {
        "linkage_radius": radius,
        "linkable_rate": linkable_rate,
        "uniqueness_rate": uniqueness_rate,
    }


