def rbf_mmd2(X: np.ndarray, Y: np.ndarray, gamma: Optional[float] = None, max_points: int = 500, seed: int = 0) -> float:
    """
    Squared MMD with RBF kernel. Subsamples to control O(n^2) cost.
    """
    rng = np.random.default_rng(seed)
    if X.shape[0] > max_points:
        X = X[rng.choice(X.shape[0], size=max_points, replace=False)]
    if Y.shape[0] > max_points:
        Y = Y[rng.choice(Y.shape[0], size=max_points, replace=False)]

    # median heuristic if gamma missing
    if gamma is None:
        Z = np.vstack([X, Y])
        if Z.shape[0] > 2 * max_points:
            Z = Z[rng.choice(Z.shape[0], size=2 * max_points, replace=False)]
        dists = pdist(Z, metric="euclidean")
        med = np.median(dists)
        gamma = 1.0 / (2.0 * med * med + 1e-12)

    def k(a: np.ndarray, b: np.ndarray) -> np.ndarray:
        D = cdist(a, b, metric="sqeuclidean")
        return np.exp(-gamma * D)

    Kxx = k(X, X)
    Kyy = k(Y, Y)
    Kxy = k(X, Y)

    m = X.shape[0]
    n = Y.shape[0]

    # unbiased estimate
    mmd2 = (Kxx.sum() - np.trace(Kxx)) / (m * (m - 1) + 1e-12) \
         + (Kyy.sum() - np.trace(Kyy)) / (n * (n - 1) + 1e-12) \
         - 2.0 * Kxy.mean()
    return float(max(mmd2, 0.0))


def jensen_shannon_divergence(p: np.ndarray, q: np.ndarray) -> float:
    p = np.asarray(p, dtype=float)
    q = np.asarray(q, dtype=float)
    p = p / (p.sum() + 1e-12)
    q = q / (q.sum() + 1e-12)
    m = 0.5 * (p + q)
    return 0.5 * (stats.entropy(p, m) + stats.entropy(q, m))


def compute_fidelity_metrics(df_real: pd.DataFrame, df_syn: pd.DataFrame, numeric_cols: List[str], categorical_cols: List[str], boolean_cols: List[str]) -> Dict[str, float]:
    metrics: Dict[str, float] = {}

    # numeric: Wasserstein distance per column
    wass = []
    for col in numeric_cols:
        a = pd.to_numeric(df_real[col], errors="coerce").dropna().to_numpy()
        b = pd.to_numeric(df_syn[col], errors="coerce").dropna().to_numpy()
        if len(a) == 0 or len(b) == 0:
            continue
        w = stats.wasserstein_distance(a, b)
        # normalize by real std to make columns comparable
        s = np.std(a) + 1e-12
        wass.append(w / s)
    metrics["numeric_wasserstein_mean"] = float(np.mean(wass) if wass else np.nan)

    # categorical/boolean: JS divergence per column
    js = []
    for col in (categorical_cols + boolean_cols):
        a = df_real[col].astype(str).fillna("MISSING")
        b = df_syn[col].astype(str).fillna("MISSING")
        cats = sorted(set(a.unique()).union(set(b.unique())))
        pa = np.array([(a == c).mean() for c in cats], dtype=float)
        pb = np.array([(b == c).mean() for c in cats], dtype=float)
        js.append(jensen_shannon_divergence(pa, pb))
    metrics["categorical_js_mean"] = float(np.mean(js) if js else np.nan)

    # correlation gap (numeric-numeric Spearman)
    if len(numeric_cols) >= 2:
        R1 = df_real[numeric_cols].astype(float).corr(method="spearman").to_numpy()
        R2 = df_syn[numeric_cols].astype(float).corr(method="spearman").to_numpy()
        gap = np.nanmean(np.abs(R1 - R2))
        metrics["spearman_corr_gap_mean"] = float(gap)
    else:
        metrics["spearman_corr_gap_mean"] = np.nan

    return metrics


def infer_task_type(y: pd.Series) -> str:
    if pd.api.types.is_numeric_dtype(y):
        # heuristic: small number of unique values -> classification
        uniq = y.dropna().unique()
        if len(uniq) <= 20 and set(map(float, uniq)) <= set(map(float, range(int(max(uniq)) + 1))):
            return "classification"
        return "regression"
    return "classification"


def train_eval_downstream(
    X_train: np.ndarray,
    y_train: np.ndarray,
    X_test: np.ndarray,
    y_test: np.ndarray,
    task_type: str,
    seed: int,
) -> Dict[str, float]:
    """
    A compact but governance-relevant benchmark:
      - Classification: LogisticRegression + RandomForest; report ROC-AUC (if binary) and macro-F1.
      - Regression: Ridge + RandomForestRegressor; report RMSE and R2.
    """
    out: Dict[str, float] = {}
    if task_type == "classification":
        # determine binary vs multiclass
        classes = np.unique(y_train[~pd.isna(y_train)])
        is_binary = len(classes) == 2

        # Logistic regression
        lr = LogisticRegression(max_iter=2000, n_jobs=None, random_state=seed)
        lr.fit(X_train, y_train)
        pred = lr.predict(X_test)
        out["lr_macro_f1"] = float(f1_score(y_test, pred, average="macro"))
        if is_binary:
            proba = lr.predict_proba(X_test)[:, 1]
            out["lr_roc_auc"] = float(roc_auc_score(y_test, proba))
            out["lr_avg_precision"] = float(average_precision_score(y_test, proba))
        else:
            out["lr_roc_auc"] = np.nan
            out["lr_avg_precision"] = np.nan

        # Random forest
        rf = RandomForestClassifier(n_estimators=250, random_state=seed, n_jobs=-1)
        rf.fit(X_train, y_train)
        pred = rf.predict(X_test)
        out["rf_macro_f1"] = float(f1_score(y_test, pred, average="macro"))
        if is_binary:
            proba = rf.predict_proba(X_test)[:, 1]
            out["rf_roc_auc"] = float(roc_auc_score(y_test, proba))
            out["rf_avg_precision"] = float(average_precision_score(y_test, proba))
        else:
            out["rf_roc_auc"] = np.nan
            out["rf_avg_precision"] = np.nan

    else:
        # Ridge regression
        rr = Ridge(alpha=1.0, random_state=seed)
        rr.fit(X_train, y_train)
        pred = rr.predict(X_test)
        rmse = float(np.sqrt(np.mean((pred - y_test) ** 2)))
        out["ridge_rmse"] = rmse
        # R2
        ss_res = float(np.sum((y_test - pred) ** 2))
        ss_tot = float(np.sum((y_test - np.mean(y_test)) ** 2) + 1e-12)
        out["ridge_r2"] = float(1.0 - ss_res / ss_tot)

        # Random forest regressor
        rf = RandomForestRegressor(n_estimators=300, random_state=seed, n_jobs=-1)
        rf.fit(X_train, y_train)
        pred = rf.predict(X_test)
        rmse = float(np.sqrt(np.mean((pred - y_test) ** 2)))
        out["rf_rmse"] = rmse
        ss_res = float(np.sum((y_test - pred) ** 2))
        ss_tot = float(np.sum((y_test - np.mean(y_test)) ** 2) + 1e-12)
        out["rf_r2"] = float(1.0 - ss_res / ss_tot)

    return out


def fairness_diagnostics_binary(
    y_true: np.ndarray,
    y_pred: np.ndarray,
    group: np.ndarray,
) -> pd.DataFrame:
    """
    Computes per-group confusion rates (binary only). Returns a table with:
      - support
      - TPR, FPR, precision, recall, F1
    """
    rows = []
    groups = pd.Series(group).astype(str).unique()
    for g in sorted(groups):
        m = (pd.Series(group).astype(str) == g).to_numpy()
        yt = y_true[m]
        yp = y_pred[m]
        if len(yt) == 0:
            continue
        tn, fp, fn, tp = confusion_matrix(yt, yp, labels=[0, 1]).ravel()
        tpr = tp / (tp + fn + 1e-12)
        fpr = fp / (fp + tn + 1e-12)
        prec = tp / (tp + fp + 1e-12)
        rec = tpr
        f1 = 2 * prec * rec / (prec + rec + 1e-12)
        rows.append(
            {
                "group": g,
                "support": int(len(yt)),
                "TPR": float(tpr),
                "FPR": float(fpr),
                "precision": float(prec),
                "recall": float(rec),
                "F1": float(f1),
            }
        )
    return pd.DataFrame(rows)


