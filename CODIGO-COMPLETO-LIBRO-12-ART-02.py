# Persistent homology up to H1 (Vietoris–Rips) with Z2 reduction
# =============================================================================

@dataclass
class Simplex:
    vertices: Tuple[int, ...]
    dim: int
    filt: float


def vr_complex_simplices(D: np.ndarray, max_dim: int = 1) -> List[Simplex]:
    """
    Build Vietoris–Rips simplices up to dimension 2 (needed to kill H1).
    Filtration values follow standard Rips: edge weight; triangle weight = max edge weight.
    """
    n = D.shape[0]
    simplices: List[Simplex] = []
    # vertices
    for i in range(n):
        simplices.append(Simplex((i,), 0, 0.0))
    # edges
    for i in range(n):
        for j in range(i + 1, n):
            simplices.append(Simplex((i, j), 1, float(D[i, j])))
    if max_dim >= 1:
        # triangles (for H1 deaths)
        for i in range(n):
            for j in range(i + 1, n):
                dij = D[i, j]
                for k in range(j + 1, n):
                    filt = float(max(dij, D[i, k], D[j, k]))
                    simplices.append(Simplex((i, j, k), 2, filt))
    # sort by filtration, then dim
    simplices.sort(key=lambda s: (s.filt, s.dim, s.vertices))
    return simplices


def simplex_boundary(simplex: Simplex, simplex_to_index: Dict[Tuple[int, ...], int]) -> int:
    """
    Returns boundary as a bitset int of row indices in simplex ordering.
    Z2 coefficients.
    """
    v = simplex.vertices
    if simplex.dim == 0:
        return 0
    # boundary is all faces of size dim
    bits = 0
    for i in range(len(v)):
        face = v[:i] + v[i + 1 :]
        idx = simplex_to_index.get(face)
        if idx is None:
            raise RuntimeError(f"Missing face {face} in complex.")
        bits ^= (1 << idx)
    return bits


def low_bit_index(bits: int) -> int:
    """
    Index of the lowest set bit (most significant in reduction sense is highest row index),
    but for column reduction we need highest row index => use bit_length-1 on bits.
    """
    return bits.bit_length() - 1


def vr_persistence_diagrams(points: np.ndarray, max_dim: int = 1) -> Dict[int, List[Tuple[float, float]]]:
    """
    Compute persistence diagrams for H0 and H1 using a basic reduction algorithm over Z2.
    This is intended for small subsamples (<= ~140 points) due to O(n^3) triangles.
    """
    # pairwise distances
    D = squareform(pdist(points, metric="euclidean"))
    simplices = vr_complex_simplices(D, max_dim=max_dim)
    simplex_to_index = {s.vertices: i for i, s in enumerate(simplices)}

    # build boundary columns (bitsets)
    boundaries: List[int] = []
    dims = [s.dim for s in simplices]
    filts = [s.filt for s in simplices]

    for s in simplices:
        b = simplex_boundary(s, simplex_to_index)
        boundaries.append(b)

    pivot_to_col: Dict[int, int] = {}
    reduced_cols: Dict[int, int] = {}

    pairs: List[Tuple[int, int]] = []

    for j in range(len(simplices)):
        col = boundaries[j]
        while col != 0:
            i = low_bit_index(col)
            if i in pivot_to_col:
                col ^= reduced_cols[pivot_to_col[i]]
            else:
                break
        if col != 0:
            i = low_bit_index(col)
            pivot_to_col[i] = j
            reduced_cols[j] = col
            pairs.append((i, j))
        else:
            reduced_cols[j] = 0

    # extract diagrams: creator i (dim k) dies at filtration of j
    diagrams: Dict[int, List[Tuple[float, float]]] = {0: [], 1: []}
    paired_creators = set(i for i, _ in pairs)

    for i, j in pairs:
        k = dims[i]
        if k in diagrams and k <= 1:
            birth = filts[i]
            death = filts[j]
            if death >= birth:
                diagrams[k].append((float(birth), float(death)))

    # unpaired creators => infinite death
    for idx in range(len(simplices)):
        k = dims[idx]
        if k <= 1 and idx not in paired_creators:
            # creator if its column reduced to zero and it created a feature of its dimension
            # Standard interpretation: columns with zero reduced boundary correspond to births.
            # Here we approximate: treat all unpaired simplices of dim 0 or 1 as infinite features.
            diagrams[k].append((float(filts[idx]), float("inf")))

    # remove trivial infinite 1D births at 0 that are artifacts (rare in practice)
    diagrams[1] = [(b, d) for (b, d) in diagrams[1] if not (b == 0.0 and math.isinf(d))]
    return diagrams


def diagram_summaries(diag: List[Tuple[float, float]]) -> Dict[str, float]:
    finite = [(b, d) for (b, d) in diag if np.isfinite(d)]
    if len(finite) == 0:
        return {"n_features": 0, "total_persistence": 0.0, "persistent_entropy": 0.0, "mean_lifetime": 0.0}
    lifetimes = np.array([d - b for b, d in finite], dtype=float)
    total = float(lifetimes.sum())
    p = lifetimes / (lifetimes.sum() + 1e-12)
    ent = float(-(p * np.log(p + 1e-12)).sum())
    return {
        "n_features": int(len(finite)),
        "total_persistence": total,
        "persistent_entropy": ent,
        "mean_lifetime": float(lifetimes.mean()),
    }


def betti_curve(diag: List[Tuple[float, float]], eps_grid: np.ndarray) -> np.ndarray:
    """
    Betti_k(eps) = number of features alive at epsilon.
    """
    b = np.zeros_like(eps_grid, dtype=float)
    for birth, death in diag:
        for i, eps in enumerate(eps_grid):
            if eps >= birth and (eps < death or math.isinf(death)):
                b[i] += 1.0
    return b


def point_to_diag_cost(p: Tuple[float, float]) -> float:
    b, d = p
    if not np.isfinite(d):
        return float("inf")
    return abs(d - b) / 2.0


def linf_dist(p: Tuple[float, float], q: Tuple[float, float]) -> float:
    return max(abs(p[0] - q[0]), abs(p[1] - q[1]))


def wasserstein_distance_diagrams(D1: List[Tuple[float, float]], D2: List[Tuple[float, float]], p: int = 1) -> float:
    """
    p-Wasserstein distance with L_infty ground metric, using linear assignment.
    Includes matching to diagonal via replicated diagonal nodes.
    """
    A = [(b, d) for (b, d) in D1 if np.isfinite(d)]
    B = [(b, d) for (b, d) in D2 if np.isfinite(d)]
    n, m = len(A), len(B)
    N = n + m
    if N == 0:
        return 0.0

    # cost matrix N x N
    C = np.zeros((N, N), dtype=float)

    # top-left: A to B
    for i in range(n):
        for j in range(m):
            C[i, j] = linf_dist(A[i], B[j])

    # A to diagonal copies (columns m..N-1)
    for i in range(n):
        c = point_to_diag_cost(A[i])
        C[i, m:] = c

    # diagonal copies of B (rows n..N-1) to B points
    for j in range(m):
        c = point_to_diag_cost(B[j])
        C[n:, j] = c

    # diagonal to diagonal: 0
    C[n:, m:] = 0.0

    row_ind, col_ind = linear_sum_assignment(C)
    costs = C[row_ind, col_ind]
    if p == 1:
        return float(costs.sum())
    return float((np.power(costs, p).sum()) ** (1.0 / p))


def hopcroft_karp(adj: List[List[int]], n_left: int, n_right: int) -> int:
    """
    Maximum bipartite matching via Hopcroft–Karp.
    adj[u] lists neighbors v in right part.
    """
    INF = 10**9
    pair_u = [-1] * n_left
    pair_v = [-1] * n_right
    dist = [0] * n_left

    from collections import deque

    def bfs() -> bool:
        q = deque()
        for u in range(n_left):
            if pair_u[u] == -1:
                dist[u] = 0
                q.append(u)
            else:
                dist[u] = INF
        found = False
        while q:
            u = q.popleft()
            for v in adj[u]:
                pu = pair_v[v]
                if pu != -1 and dist[pu] == INF:
                    dist[pu] = dist[u] + 1
                    q.append(pu)
                if pu == -1:
                    found = True
        return found

    def dfs(u: int) -> bool:
        for v in adj[u]:
            pu = pair_v[v]
            if pu == -1 or (dist[pu] == dist[u] + 1 and dfs(pu)):
                pair_u[u] = v
                pair_v[v] = u
                return True
        dist[u] = INF
        return False

    matching = 0
    while bfs():
        for u in range(n_left):
            if pair_u[u] == -1:
                if dfs(u):
                    matching += 1
    return matching


def bottleneck_distance_diagrams(D1: List[Tuple[float, float]], D2: List[Tuple[float, float]], tol: float = 1e-6) -> float:
    """
    Bottleneck distance via binary search + feasibility (perfect matching) under threshold eps.
    Uses the diagonal-augmentation trick with replicated diagonal nodes.
    """
    A = [(b, d) for (b, d) in D1 if np.isfinite(d)]
    B = [(b, d) for (b, d) in D2 if np.isfinite(d)]
    n, m = len(A), len(B)
    N = n + m
    if N == 0:
        return 0.0

    # precompute all candidate costs
    costs = []
    for i in range(n):
        costs.append(point_to_diag_cost(A[i]))
        for j in range(m):
            costs.append(linf_dist(A[i], B[j]))
    for j in range(m):
        costs.append(point_to_diag_cost(B[j]))
    costs = sorted(set([float(c) for c in costs if np.isfinite(c)]))
    if not costs:
        return 0.0

    def feasible(eps: float) -> bool:
        # build bipartite graph: left size N (A points + m diag copies for B), right size N (B points + n diag copies for A)
        adj = [[] for _ in range(N)]
        # left A points (0..n-1)
        for i in range(n):
            # connect to B points
            for j in range(m):
                if linf_dist(A[i], B[j]) <= eps:
                    adj[i].append(j)
            # connect to any diagonal node on right (m..N-1) if diag cost ok
            if point_to_diag_cost(A[i]) <= eps:
                adj[i].extend(list(range(m, N)))
        # left diag copies for B (n..N-1), each corresponds to some B[j]
        for j in range(m):
            u = n + j
            # can match to B[j] if diag cost ok
            if point_to_diag_cost(B[j]) <= eps:
                adj[u].append(j)
            # can match to any diagonal node (always 0)
            adj[u].extend(list(range(m, N)))

        match = hopcroft_karp(adj, n_left=N, n_right=N)
        return match == N

    # binary search on candidate list
    lo, hi = 0, len(costs) - 1
    best = costs[-1]
    while lo <= hi:
        mid = (lo + hi) // 2
        eps = costs[mid]
        if feasible(eps):
            best = eps
            hi = mid - 1
        else:
            lo = mid + 1
    return float(best)


