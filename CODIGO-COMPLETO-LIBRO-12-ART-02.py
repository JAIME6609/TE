class BootstrapNoiseGenerator(BaseGenerator):
    name = "bootstrap"

    def __init__(self, numeric_cols: List[str], noise_scale: float):
        self.numeric_cols = numeric_cols
        self.noise_scale = noise_scale
        self.df_train: Optional[pd.DataFrame] = None
        self._num_std: Optional[pd.Series] = None

    def fit(self, df_train: pd.DataFrame, pre: MixedTabularPreprocessor, config: RunConfig) -> "BootstrapNoiseGenerator":
        self.df_train = df_train.reset_index(drop=True).copy()
        if self.numeric_cols:
            self._num_std = self.df_train[self.numeric_cols].astype(float).std(ddof=0).replace(0.0, 1.0)
        return self

    def sample(self, n: int, seed: int) -> pd.DataFrame:
        assert self.df_train is not None
        rng = np.random.default_rng(seed)
        idx = rng.integers(0, len(self.df_train), size=n)
        syn = self.df_train.iloc[idx].copy().reset_index(drop=True)
        if self.numeric_cols:
            std = self._num_std
            assert std is not None
            noise = rng.normal(0.0, 1.0, size=(n, len(self.numeric_cols))) * (std.values * self.noise_scale)
            syn[self.numeric_cols] = syn[self.numeric_cols].astype(float).values + noise
            # clip to observed bounds
            for col in self.numeric_cols:
                col_min = pd.to_numeric(self.df_train[col], errors="coerce").min()
                col_max = pd.to_numeric(self.df_train[col], errors="coerce").max()
                syn[col] = syn[col].clip(col_min, col_max)
        return syn


class GaussianCopulaGenerator(BaseGenerator):
    name = "copula"

    def __init__(self, numeric_cols: List[str], categorical_cols: List[str], boolean_cols: List[str]):
        self.numeric_cols = numeric_cols
        self.categorical_cols = categorical_cols
        self.boolean_cols = boolean_cols

        self.df_train: Optional[pd.DataFrame] = None
        self._corr: Optional[np.ndarray] = None
        self._num_quantiles: Dict[str, Tuple[np.ndarray, np.ndarray]] = {}
        self._cat_freq: Dict[str, Tuple[np.ndarray, np.ndarray]] = {}
        self._bool_freq: Dict[str, Tuple[np.ndarray, np.ndarray]] = {}

    @staticmethod
    def _rank_to_normal(x: np.ndarray) -> np.ndarray:
        r = stats.rankdata(x, method="average")
        u = (r - 0.5) / len(x)
        return stats.norm.ppf(u)

    def fit(self, df_train: pd.DataFrame, pre: MixedTabularPreprocessor, config: RunConfig) -> "GaussianCopulaGenerator":
        self.df_train = df_train.reset_index(drop=True).copy()

        # numeric copula fit
        if self.numeric_cols:
            Z = []
            for col in self.numeric_cols:
                x = pd.to_numeric(self.df_train[col], errors="coerce").to_numpy()
                x = np.nan_to_num(x, nan=np.nanmedian(x))
                z = self._rank_to_normal(x)
                Z.append(z)
                # store inverse empirical CDF via quantiles
                qs = np.linspace(0.0, 1.0, 1001)
                vals = np.quantile(x, qs)
                self._num_quantiles[col] = (qs, vals)
            Z = np.vstack(Z).T
            self._corr = np.corrcoef(Z, rowvar=False)
            # numerical stability
            self._corr = np.nan_to_num(self._corr, nan=0.0)
            self._corr = (self._corr + self._corr.T) / 2.0
            np.fill_diagonal(self._corr, 1.0)

        # categorical + boolean fit: empirical frequencies
        for col in self.categorical_cols:
            vc = self.df_train[col].astype(str).fillna("MISSING").value_counts(dropna=False)
            values = vc.index.to_numpy()
            probs = (vc / vc.sum()).to_numpy()
            self._cat_freq[col] = (values, probs)

        for col in self.boolean_cols:
            vc = self.df_train[col].astype(str).fillna("MISSING").value_counts(dropna=False)
            values = vc.index.to_numpy()
            probs = (vc / vc.sum()).to_numpy()
            self._bool_freq[col] = (values, probs)

        return self

    def sample(self, n: int, seed: int) -> pd.DataFrame:
        assert self.df_train is not None
        rng = np.random.default_rng(seed)
        out: Dict[str, Any] = {}

        # numeric
        if self.numeric_cols:
            corr = self._corr
            assert corr is not None
            # sample multivariate normal
            L = np.linalg.cholesky(corr + 1e-6 * np.eye(len(self.numeric_cols)))
            Z = rng.normal(size=(n, len(self.numeric_cols))) @ L.T
            U = stats.norm.cdf(Z)
            for j, col in enumerate(self.numeric_cols):
                qs, vals = self._num_quantiles[col]
                u = np.clip(U[:, j], 0.0, 1.0)
                out[col] = np.interp(u, qs, vals)

            # clip and round integer-like columns
            for col in self.numeric_cols:
                col_min = pd.to_numeric(self.df_train[col], errors="coerce").min()
                col_max = pd.to_numeric(self.df_train[col], errors="coerce").max()
                v = np.clip(out[col], col_min, col_max)
                if pd.api.types.is_integer_dtype(self.df_train[col]) or (self.df_train[col].dropna().apply(float.is_integer).mean() > 0.95):
                    v = np.round(v).astype(int)
                out[col] = v

        # categorical + boolean
        for col, (values, probs) in self._cat_freq.items():
            out[col] = rng.choice(values, size=n, replace=True, p=probs)

        for col, (values, probs) in self._bool_freq.items():
            out[col] = rng.choice(values, size=n, replace=True, p=probs)

        return pd.DataFrame(out)


class TabularVAE(nn.Module):
    def __init__(self, input_dim: int, latent_dim: int, hidden_dim: int):
        super().__init__()
        self.enc1 = nn.Linear(input_dim, hidden_dim)
        self.enc2 = nn.Linear(hidden_dim, hidden_dim)
        self.mu = nn.Linear(hidden_dim, latent_dim)
        self.logvar = nn.Linear(hidden_dim, latent_dim)

        self.dec1 = nn.Linear(latent_dim, hidden_dim)
        self.dec2 = nn.Linear(hidden_dim, hidden_dim)
        self.out = nn.Linear(hidden_dim, input_dim)

    def encode(self, x: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor]:
        h = F.relu(self.enc1(x))
        h = F.relu(self.enc2(h))
        return self.mu(h), self.logvar(h)

    def reparameterize(self, mu: torch.Tensor, logvar: torch.Tensor) -> torch.Tensor:
        std = torch.exp(0.5 * logvar)
        eps = torch.randn_like(std)
        return mu + eps * std

    def decode(self, z: torch.Tensor) -> torch.Tensor:
        h = F.relu(self.dec1(z))
        h = F.relu(self.dec2(h))
        return self.out(h)

    def forward(self, x: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        mu, logvar = self.encode(x)
        z = self.reparameterize(mu, logvar)
        recon = self.decode(z)
        return recon, mu, logvar


class VAEGenerator(BaseGenerator):
    name = "vae"

    def __init__(self, pre: MixedTabularPreprocessor, cfg: GeneratorConfig, device: torch.device):
        self.pre = pre
        self.cfg = cfg
        self.device = device
        self.model: Optional[TabularVAE] = None
        self.reference_df: Optional[pd.DataFrame] = None

    def fit(self, df_train: pd.DataFrame, pre: MixedTabularPreprocessor, config: RunConfig) -> "VAEGenerator":
        self.reference_df = df_train.copy()
        X = self.pre.transform(df_train)
        # Subsample to keep training feasible for large datasets (governance-friendly evaluation budgets)
        if X.shape[0] > self.cfg.vae_max_train_rows:
            rng = np.random.default_rng(12345)
            idx = rng.choice(X.shape[0], size=self.cfg.vae_max_train_rows, replace=False)
            X = X[idx]
        X_t = torch.from_numpy(X).to(self.device)

        ds = TensorDataset(X_t)
        dl = DataLoader(ds, batch_size=self.cfg.vae_batch_size, shuffle=True, drop_last=False)

        model = TabularVAE(input_dim=X.shape[1], latent_dim=self.cfg.vae_latent_dim, hidden_dim=self.cfg.vae_hidden_dim).to(self.device)
        opt = torch.optim.Adam(model.parameters(), lr=self.cfg.vae_lr)

        model.train()
        for epoch in range(1, self.cfg.vae_epochs + 1):
            losses = []
            for (xb,) in dl:
                opt.zero_grad()
                recon, mu, logvar = model(xb)
                # reconstruction loss: MSE on full vector (scaled numeric + one-hot)
                rec_loss = F.mse_loss(recon, xb, reduction="mean")
                kl = -0.5 * torch.mean(1 + logvar - mu.pow(2) - logvar.exp())
                loss = rec_loss + self.cfg.vae_beta * kl
                loss.backward()
                opt.step()
                losses.append(float(loss.detach().cpu().item()))
            # minimal verbosity: keep last
        self.model = model
        return self

    def sample(self, n: int, seed: int) -> pd.DataFrame:
        if self.model is None or self.reference_df is None:
            raise RuntimeError("VAEGenerator must be fitted before sampling.")
        set_global_seed(seed)
        self.model.eval()
        with torch.no_grad():
            z = torch.randn((n, self.cfg.vae_latent_dim), device=self.device)
            x_hat = self.model.decode(z).cpu().numpy()
        # inverse to dataframe
        syn = self.pre.inverse_samples(x_hat, reference_df=self.reference_df)
        return syn


